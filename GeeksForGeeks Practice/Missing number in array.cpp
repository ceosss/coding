#include <bits/stdc++.h>
using namespace std;
int main()
{
    int t;
    cin >> t;
    while (t--)
    {
        int n, x;
        cin >> n;
        int original = 0;
        int missing = 0;
        for (int i = 1; i <= n; i++)
        {
            original = original ^ i;
        }
        for (int i = 1; i < n; i++)
        {
            cin >> x;
            missing = missing ^ x;
        }
        cout << (missing ^ original) << endl;
    }
    return 0;
}

