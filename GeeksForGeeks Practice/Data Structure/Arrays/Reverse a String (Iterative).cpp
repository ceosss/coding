#include <bits/stdc++.h>

#define ll long long
using namespace std;

int main()
{
    int t;
    cin >> t;
    while (t--)
    {
        string s;
        cin >> s;
        int n = s.length();
        for (int i = 0; i < n / 2; i++)
        {
            char c = s[i];
            s[i] = s[n - i - 1];
            s[n - i - 1] = c;
        }
        cout << s << endl;
    }
    return 0;
}

