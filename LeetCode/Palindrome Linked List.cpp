/**
 * Definition for singly-linked list.
 * struct ListNode {
 *     int val;
 *     ListNode *next;
 *     ListNode() : val(0), next(nullptr) {}
 *     ListNode(int x) : val(x), next(nullptr) {}
 *     ListNode(int x, ListNode *next) : val(x), next(next) {}
 * };
 */
class Solution
{
public:
    bool isPalindrome(ListNode *head)
    {
        ListNode *temp = head;
        stack<int> a;
        while (temp)
        {
            a.push(temp->val);
            temp = temp->next;
        }
        while (head != NULL)
        {
            int t = a.top();
            a.pop();
            if (t != head->val)
                return false;
            head = head->next;
        }
        return true;
    }
};

