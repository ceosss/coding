class Solution
{
public:
    vector<int> smallerNumbersThanCurrent(vector<int> &nums)
    {
        vector<int> b;
        int n = nums.size();
        for (int i = 0; i < n; i++)
        {
            int count = 0;
            for (int j = 0; j < n; j++)
            {
                if (i != j && nums[i] > nums[j])
                {
                    count++;
                }
            }
            b.push_back(count);
        }
        return b;
    }
};

