#include <bits/stdc++.h>
using namespace std;

#define ll long long

int main()
{
    ll t;
    cin >> t;
    while (t--)
    {
        int a, b;
        cin >> a >> b;
        int ans = min(a, b);
        cout << min(ans, (a + b) / 3) << endl;
    }
    return 0;
}

