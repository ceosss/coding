#include <bits/stdc++.h>

#define ll long long
using namespace std;

int main()
{
    int n, k, l, c, d, p, nl, np;
    cin >> n >> k >> l >> c >> d >> p >> nl >> np;
    int drink = k * l;
    int toasts = drink / nl;
    int limes = c * d;
    int salt = p / np;
    cout << min(toasts, min(limes, salt)) / n << endl;

    return 0;
}

