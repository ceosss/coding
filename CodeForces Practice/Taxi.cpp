#include <bits/stdc++.h>
using namespace std;

int main()
{
    int n;
    cin >> n;
    int a[n];
    for (int i = 0; i < n; i++)
    {
        cin >> a[i];
    }
    sort(a, a + n);

    int p = n - 1;
    int i = 0;
    int c = 0;

    while (p != i)
    {
        if (a[p] + a[i] <= 4)
        {

            a[p] = a[p] + a[i];
            i++;
        }
        else
        {
            p--;
            c++;
        }
    }
    cout << c + 1 << endl;

    return 0;
}

