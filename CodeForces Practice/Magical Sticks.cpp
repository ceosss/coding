#include <bits/stdc++.h>

#define ll long long
using namespace std;

void swap(int *a, int *b)
{
    int temp = *a;
    *a = *b;
    *b = temp;
}

int main()
{
    int t;
    cin >> t;
    while (t--)
    {
        ll n;
        cin >> n;
        cout << (n + 1) / 2 << endl;
    }
    return 0;
}

