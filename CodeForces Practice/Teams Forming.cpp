#include <bits/stdc++.h>

#define ull unsigned long long
#define ll long long

using namespace std;

int main()
{
    int n;
    cin >> n;
    int a[n];
    for (int i = 0; i < n; i++)
    {
        cin >> a[i];
    }
    sort(a, a + n);
    int problems = 0;
    for (int i = 0; i < n; i += 2)
    {
        problems += abs(a[i] - a[i + 1]);
    }
    cout << problems << endl;
    return 0;
}

