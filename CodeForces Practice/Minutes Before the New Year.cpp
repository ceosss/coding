#include <bits/stdc++.h>

#define ll long long
using namespace std;

int main()
{
    ll t;
    cin >> t;
    while (t--)
    {
        int h, m;
        cin >> h >> m;
        cout << (24 - h) * 60 - m << endl;
    }
    return 0;
}

